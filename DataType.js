/* As we know JavaScript DataTypes Are very Dynamic It's type can mutate through runtime */

/* There are seven Data types which is distributed into primitive and non-primitive */

/* Primitive */

// String
let UserName = "Abuzar"
console.log('String', UserName)


// Number
let Age = 22
console.log('Number', Age)


// Boolean
let isAlive = true
console.log('Boolean', isAlive)


// Undefined
let future
console.log('Undefined', future)

// Null
let WifeName = null
console.log('Null', WifeName)

// BigInt
